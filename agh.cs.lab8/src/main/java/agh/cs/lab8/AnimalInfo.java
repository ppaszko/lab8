package agh.cs.lab8;

public class AnimalInfo {
    public int id;
    public int age;
    public int day;

    public int kids;
    public int descendants;
    public int days;

    public AnimalInfo(int id, int age, int day, int kids, int Descendants, int days) {
        this.id = id;
        this.age = age;
        this.day = day;
        this.kids = kids;
        this.descendants = Descendants;
        this.days = days;
    }
}
